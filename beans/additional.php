<?php
class additional {
	private $otwage;
	private $ewage;
	
	public function getOtwage(){
		return $this->otwage;
	}

	public function setOtwage($otwage){
		$this->otwage = $otwage;
	}

	public function getEwage(){
		return $this->ewage;
	}

	public function setEwage($ewage){
		$this->ewage = $ewage;
	}
}